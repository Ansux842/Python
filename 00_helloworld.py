# Con type podemos imprimir por pantalla el tipo de dato que contenga.
print(type("Hola Python!")) # Tipo 'str' cadena de texto.
print(type(5)) # Tipo 'int' numero entero.
print(type(1.5)) # Tipo 'float' numero flotante.
print(type(3 + 1j)) # Tipo 'complex' numero complejo.
print(type(True)) # Tipo 'bool' dato booleano.
print(type(print("Mi cadena de texto"))) # Tipo 'NoneType'